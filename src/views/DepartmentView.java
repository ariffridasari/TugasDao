    public void resetText(){
        txtid.setText("");
        txtname.setText("");
        txtmid.setText("");
        txtlocid.setText("");
        txtid.setEditable(true);
        insert.setEnabled(true);
    }
    
    public void showTable(){
        DefaultTableModel model = (DefaultTableModel)tableDepartment.getModel();
        Object[] row = new Object[4];
        List<Department> department = new ArrayList<>();
        department = idc.getAll();
        for (int i = 0; i < department.size(); i++) {
            row[0]=department.get(i).getId();
            row[1]=department.get(i).getName();
            row[2]=department.get(i).getManager_id();
            row[3]=department.get(i).getLocation_id();
            model.addRow(row);
        }
    }
    public void showTable(String s){
        DefaultTableModel model = (DefaultTableModel)tableDepartment.getModel();
        Object[] row = new Object[5];
        List<Department> department =  new ArrayList<>();
        department = idc.search(s);
        for (int i = 0; i < department.size(); i++) {
            row[0]=department.get(i).getId();
            row[1]=department.get(i).getName();
            row[2]=department.get(i).getManager_id();
            row[3]=department.get(i).getLocation_id();
            model.addRow(row);
        }
    }
    
    public void updateTable(){
        DefaultTableModel model = (DefaultTableModel)tableDepartment.getModel();
        model.setRowCount(0);
        showTable();
    }
    
    public void updateTable(String s){
        DefaultTableModel model = (DefaultTableModel)tableDepartment.getModel();
        model.setRowCount(0);
        if(s == ""){
            showTable();
        }
        showTable(s);
    }

