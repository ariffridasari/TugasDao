    private void tableDepartmentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDepartmentMouseClicked
        DefaultTableModel model = (DefaultTableModel)tableDepartment.getModel();
        int SelectedRowIndex = tableDepartment.getSelectedRow();
        txtid.setEditable(false);
        insert.setEnabled(false);
        txtid.setText(model.getValueAt(SelectedRowIndex, 0).toString());
        txtname.setText(model.getValueAt(SelectedRowIndex, 1).toString());
        txtmid.setText(model.getValueAt(SelectedRowIndex, 2).toString());
        txtlocid.setText(model.getValueAt(SelectedRowIndex, 3).toString());
    }//GEN-LAST:event_tableDepartmentMouseClicked

